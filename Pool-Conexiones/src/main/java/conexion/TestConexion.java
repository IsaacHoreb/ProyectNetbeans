    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Connection conexion = null;
        PreparedStatement consulta = null;
        ResultSet resultado = null;

        try {
            conexion = instanciaConexion.conectar();
            consulta = conexion.prepareStatement("SELECT * FROM cliente");

            resultado = consulta.executeQuery();

            if (resultado.next()) {
                JOptionPane.showMessageDialog(null, "Existen datos");
            } else {
                JOptionPane.showMessageDialog(null, "No existe datos");
            }

        } catch (SQLException e) {
            System.out.println(e);
        } finally {
            instanciaConexion.cerrarStatement(consulta);
            instanciaConexion.cerrarResulSet(resultado);
            instanciaConexion.desconectar(conexion);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

